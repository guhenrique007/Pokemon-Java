package pacote;

public class Character{

    private int life;
    private int attack;
    
    
     public Character(int life, int attack) {
        this.attack = attack;
        this.life = life;
    }

     
     
     
     
     
     
     
    //GETTERS AND SETTERS
    public int getLife() {
        return life;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }

     
     
}